def rotateArray(array_2d):
  list_of_tuples = zip(*array_2d[::-1])
  return [list(elem) for elem in list_of_tuples]

def rotate(matrix):
  temp_matrix = []
  column = len(matrix)-1
  for column in range(len(matrix)):
      temp = []
      for row in range(len(matrix)-1,-1,-1):
        temp.append(matrix[row][column])
      temp_matrix.append(temp)
  for i in range(len(matrix)):
      for j in range(len(matrix)):
        matrix[i][j] = temp_matrix[i][j]
  return matrix

def getDistance(X1, Y1, X2, Y2):
    dist = Math.fabs(X2 - X1) + Math.fabs(Y2 - Y1)
    return int(dist)


class Spell:
  def getHitCells(fromX, fromY, targetX, targetY, cellUnwalkable, cellWall, spell, cellMonsters, mapWidth = 33, mapHeight = 34):
    spellZone = spell.get("zone")
    cellsNonLos = cellWall.copy()
    cellsNonLos.extend(cellMonsters)
    

    hittingCells = []

    topSpellZone = spellZone

    leftSpellZone = spellZone[::-1]
    leftSpellZone = rotateArray(leftSpellZone)
    leftSpellZone = leftSpellZone[::-1]

    rightSpellZone = rotateArray(spellZone)

    bottomSpellZone = spellZone[::-1]

    finalSpellZone = spellZone

    
    hitPoint = [4, 4]
    
    spellXLen = len(spellZone)
    spellYLen = len(spellZone[0])

    for x in range(targetX - (spellXLen - hitPoint[0]) + 1, targetX +  hitPoint[0] + 1):
      for y in range(targetY - (spellYLen - hitPoint[1]) + 1, targetY +  hitPoint[1] + 1):
        
        if spell.get('align'):
          if x == fromX and y > fromY: 
            finalSpellZone = rightSpellZone
          #left
          elif x == fromX and y < fromY:
            finalSpellZone = leftSpellZone

          # top
          elif x < fromX and y == fromY:
            finalSpellZone = topSpellZone

          # bottom
          elif x > fromX and y == fromY:
            finalSpellZone = bottomSpellZone
          
          else:
            continue

        # Check si le point est bien dans le tableau 
        if x >= 0 and y >= 0 and x < mapHeight and y < mapWidth:

          # Si n'est pas un mur, une cell non walkable:
          spellTargetX = targetX - x + hitPoint[0]
          spellTargetY = targetY - y + hitPoint[1] 
          if spellTargetX >= 0 and spellTargetX < len(finalSpellZone) and spellTargetY >= 0 and spellTargetY < len(finalSpellZone[spellTargetX]):
            
            if finalSpellZone[spellTargetX][spellTargetY] != -1  and [x, y] not in cellUnwalkable and [x, y] not in cellWall:

              distance = getDistance(fromX, fromY, x, y)

              # Si la distance est accessible avec la PO du sort
              if distance <= spell.get('PO') and distance >= spell.get('POmin'):
                # Si On a la ligne de vue
                if LineOfSight.isReachable(fromX, fromY, x, y, cellsNonLos):
                  hittingCells.append({
                    'spellId': spell['id'],
                    'coords': [x, y]
                  })

    if len(hittingCells) == 0:
      return -1

    return hittingCells
  
  
